# но в другую сторону, то есть суммарный угол поворота стрелок равен полному обороту.
# Для каждой стрелки независимо вычислим её позицию. Для часовой это 12 минус текущая позиция,
# а для минутной — m минус текущая позиция. В обоих случая надо не забыть заменить 12 или 60 на ноль.
